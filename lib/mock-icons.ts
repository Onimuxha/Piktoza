export function generateMockIcons(): Icon[] {
  const icons: Icon[] = []

  iconNames.forEach((name, index) => {
    const category = categories[index % categories.length]
    icons.push({ name, category })
  })

  // Generate more icons to reach 3000+
  for (let i = iconNames.length; i < 3000; i++) {
    const baseName = iconNames[i % iconNames.length]
    const suffix = Math.floor(i / iconNames.length) + 1
    const category = categories[i % categories.length]
    icons.push({
      name: `${baseName}${suffix}`,
      category,
    })
  }

  return icons
}
